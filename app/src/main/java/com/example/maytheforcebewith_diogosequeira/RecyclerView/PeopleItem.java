package com.example.maytheforcebewith_diogosequeira.RecyclerView;

public class PeopleItem {

    private int icon;
    private String name;


    public PeopleItem(int icon, String name){

        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }

}
